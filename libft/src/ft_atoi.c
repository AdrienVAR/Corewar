int		ft_atoi(const char *str)
{
	long int nb;
	long int sg;

	nb = 0;
	sg = 1;
	while (*str == 32 || (*str > 8 && *str < 14))
		str++;
	if (*str == '+' || *str == '-')
		sg = *str++ == '+' ? 1 : -1;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + (*str++ - '0');
		if ((nb > 0x7FFFFFFF && sg > 0) || (nb > 0x80000000 && sg < 0))
			return (0);
	}
	return (nb * sg);
}
