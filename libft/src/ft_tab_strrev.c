#include "libft.h"

void			ft_tab_strrev(char **tab)
{
	int		i;
	int		j;

	i = -1;
	j = 0;
	while (tab[j])
		j++;
	while (++i < --j)
		ft_strswap(&tab[i], &tab[j]);
}
