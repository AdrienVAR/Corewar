#include "libft.h"

void	ft_putnbr(int n)
{
	long int ns;

	ns = n;
	if (ns < 0)
	{
		ns = -ns;
		ft_putchar('-');
	}
	if (ns < 10)
	{
		ft_putchar('0' + ns);
		return ;
	}
	ft_putnbr(ns / 10);
	ft_putnbr(ns % 10);
}
